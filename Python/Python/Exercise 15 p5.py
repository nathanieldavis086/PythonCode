import turtle
turtle.penup()
turtle.goto(0,-20)
turtle.pendown()
turtle.circle(20)
turtle.penup()
turtle.goto(0,0)
turtle.pendown()
turtle.goto(200,0)
turtle.penup()
turtle.goto(200,10)
turtle.write("East")
turtle.penup()
turtle.left(180)
turtle.goto(0,0)
turtle.pendown()
turtle.goto(-200,0)
turtle.penup()
turtle.goto(-200,0)
turtle.pendown()
turtle.write("West")
turtle.penup()
turtle.goto(0,0)
turtle.left(90)
turtle.pendown()
turtle.goto(0,200)
turtle.penup()
turtle.goto(10,200)
turtle.write("North")
turtle.penup()
turtle.goto(0,0)
turtle.pendown()
turtle.goto(0,-200)
turtle.penup()
turtle.goto(10,-200)
turtle.pendown()
turtle.write("South")
turtle.done()